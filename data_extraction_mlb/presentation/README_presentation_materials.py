# 2. YOLO 모델 훈련 결과 그래프들
# ===========================================

"""
YOLO(You Only Look Once) 모델은 투수를 찾아내는 AI입니다.

📊 주요 그래프 파일들:
├── results.png              # 종합 성능 대시보드
├── BoxF1_curve.png         # F1 스코어 변화 그래프
├── BoxP_curve.png          # 정밀도(Precision) 그래프
├── BoxR_curve.png          # 재현율(Recall) 그래프
├── confusion_matrix.png     # 예측 정확도 표
└── confusion_matrix_normalized.png  # 정규화된 정확도 표

📝 하나씩 설명:

1) results.png - 종합 성능 대시보드
   - YOLO 모델이 학습하면서 점점 똑똑해지는 과정을 보여줍니다
   - 왼쪽 위: 정확도 그래프 (파란선 = 학습 데이터, 주황선 = 테스트 데이터)
   - 오른쪽 위: 손실 그래프 (낮을수록 좋음)
   - 왼쪽 아래: 박스 정확도 (얼마나 정확히 사각형을 그렸는지)
   - 오른쪽 아래: 클래스별 정확도 (투수 감지 정확도)

2) BoxF1_curve.png - F1 스코어 변화
   - F1 스코어 = (정밀도 + 재현율) / 2
   - AI가 투수를 얼마나 잘 찾고 정확히 표시하는지 종합 점수
   - 높을수록 좋음 (최대 1.0)

3) BoxP_curve.png - 정밀도 그래프
   - "AI가 투수라고 찾은 것 중 실제 투수가 몇 %인지"
   - 예: 95% 정밀도 = 100개 찾은 것 중 95개가 실제 투수

4) BoxR_curve.png - 재현율 그래프
   - "실제 투수 중 AI가 몇 %를 찾았는지"
   - 예: 90% 재현율 = 100명의 투수 중 90명을 찾음

5) confusion_matrix.png - 혼동 행렬
   - 표 형태로 AI 예측이 얼마나 맞았는지 보여줍니다
   - 대각선이 밝을수록 정확함 (파란색 = 맞춤, 빨간색 = 틀림)

🎯 발표 활용:
- AI 모델이 어떻게 학습하고 발전하는지 보여줄 때 사용
- 기술적 깊이를 보여주면서도 이해하기 쉽게 설명 가능
"""

